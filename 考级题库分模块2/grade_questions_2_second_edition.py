# 简介：考级题库中保存了历年来每个等级常考的重点选择题，这些题目均是由老师整理保存到txt文本文件中，这些数据都保存在了考级题库文件夹中，在模块一中，保存的数据从文件夹中读取出来，并保存在了level1、level2、level3、level4、level5这5个变量中，而模块二我们需要判断当使用程序的用户点击了哪个按钮，并将正确的数据进行处理，返回处理后的结果
# 需要完成：用户点击的选项文字将会传入到参数choice中，先判断用户选择的级别，并将对应级别的变量保存在data变量中，最后使用字符串剪切功能处理data变量，处理成一个列表，每个题目作为一个单独的元素，并返回处理后的结果
# 原始数据格式：'1.以下哪个...///2.以下关于....///3.以下选项中'
# 目标处理后的格式：['1.以下哪个...' , '2.以下关于....' , '3.以下选项中']
# 提示：可能会用到的代码：if、elif、return、split


# 单词翻译：handle（处理）、choice（选项）
def handle_kj_data(choice):
    # 定义一个空变量data，用来保存结果
    data = ''
    # 判断choice是否为'一级'
    if choice == '一级':
        # 如果是，则将level1中的数据保存到data中
        data == level1
    # 判断choice是否为'二级'
    elif choice == '二级':
        # 如果是，则将level2中的数据保存到data中
        data == level2
    # 判断choice是否为'三级'
    elif choice == '三级':
        # 如果是，则将level3中的数据保存到data中
        data == level3
    # 判断choice是否为'四级'
    elif choice == '四级':
        # 如果是，则将level4中的数据保存到data中
        data == level4
    # 判断choice是否为'五级'
    elif choice == '五级':
        # 如果是，则将level5中的数据保存到data中
        data == level5
    # 仔细观察数据格式，将储存了对应级别数据的data变量剪切，处理
    # 成一个列表，每个题目作为一个单独的元素，使用另一个变量
    # 接收处理后的返回值
    tikuu = data.split('///')
    # 使用return返回处理后的结果
    return tikuu

